// Crear las variables num1, num2 y resultado
let num1, num2, resultado;

// Pedir al usuario que asigne un valor a num1 y num2
num1 = parseInt(prompt("Por favor, ingresa el primer número:"));
num2 = parseInt(prompt("Por favor, ingresa el segundo número:"));

// Calcular la suma y asignarla a la variable resultado
resultado = num1 + num2;

// Mostrar por consola el valor de resultado
alert("La suma de " + num1 + " y " + num2 + " es " + resultado);
